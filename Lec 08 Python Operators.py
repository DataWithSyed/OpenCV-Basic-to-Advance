# Python operators which are addition, subtraction, division, multiplication, remainder after division, exponential function.

# Addition function
add = 4+8
print(add)

# Subtraction function
subtract = 9-22
print(subtract)

# Multiplication function
multiply = 9*9
print(multiply)

# Division function for float
divide = 78 / 7
print(divide)

# Division function for integer
divide1 = 88 // 9
print(divide1)

# Remainder function
remainder = 98 % 5
print(remainder)

# Exponential function
exponent = 5 ** 6
print(exponent)

# Mixed function from left it will start first exponent, multiply, divide, subtract, add
mix = 6+9-8/7**8*7
print(mix)